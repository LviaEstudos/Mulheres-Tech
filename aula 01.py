# Código para o usuário inserir dois números e retornar todas as operações básicas e o módulo

print('MULHERES TECH COPACABANA')

print('Digite o primerio número')
N1 = int(input())

print('Digite o segundo número')
N2 = int(input())

soma = N1 + N2
subtracao = N1 - N2
multiplicacao = N1 * N2
divisao = N1 / N2
modulo = N1 % N2

print('O resultado da soma é:' , soma)
print('O resultado da subtração é:' , subtracao)
print('O resultado da multiplicação é:' , multiplicacao)
print('O resultado da divisão é:' , divisao)
print('O resultado do módulo é:' , modulo)
